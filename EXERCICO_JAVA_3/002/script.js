// a) Criar variáveis numero1 e numero2 e realizar operações matemáticas
let numero1 = 10;
let numero2 = 5;

console.log("Soma:", numero1 + numero2);         // Saída: 15
console.log("Subtração:", numero1 - numero2);    // Saída: 5
console.log("Multiplicação:", numero1 * numero2); // Saída: 50
console.log("Divisão:", numero1 / numero2);      // Saída: 2

// b) Calcular o quadrado de um número fornecido pelo usuário
let numero = parseInt(prompt("Digite um número:")); // Suponha que o usuário digite 4
let quadrado = numero * numero;
console.log("O quadrado de " + numero + " é " + quadrado); // Saída: O quadrado de 4 é 16
