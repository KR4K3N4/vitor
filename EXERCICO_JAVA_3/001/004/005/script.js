// a) Função soma que recebe dois números e retorna a soma deles
function soma(a, b) {
    return a + b;
}

console.log(soma(4, 5)); // Saída: 9

// b) Função ehPar que verifica se um número é par
function ehPar(numero) {
    return numero % 2 === 0;
}

console.log(ehPar(4)); // Saída: true
console.log(ehPar(7)); // Saída: false
